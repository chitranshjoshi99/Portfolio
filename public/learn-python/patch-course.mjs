// ---------- 5. Extra questions per existing module ----------
// Each question object: {t, card, mission, ctx?, ph, sol, re, ok, checks}
// Regex authoring rule: the regex tester norms the sol (trim + trailing space strip) and
// tests it as a single string; anchor with ^$; escape braces { } and dots.

const q = (t, card, mission, ph, sol, re, ok, checks, ctx) => {
  const o = { t, card, mission };
  if (ctx !== undefined) o.ctx = ctx;
  o.ph = ph; o.sol = sol; o.re = re; o.ok = ok; o.checks = checks;
  return o;
};


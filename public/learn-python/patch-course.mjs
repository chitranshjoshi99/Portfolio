// One-shot mutator for course.json to address GAPS.md.
// Run: node patch-course.mjs && node verify-course.mjs
import { readFileSync, writeFileSync } from 'node:fs';

const PATH = new URL('./course.json', import.meta.url);
const course = JSON.parse(readFileSync(PATH));

// ---------- helpers ----------
const findMod = id => course.modules.find(m => m.id === id);
const findCp = (m, id) => m.checkpoints.find(cp => cp.id === id);
const upsertBadge = (id, name, how, color, pos, geo, artifact) => {
  const existing = course.badges.find(b => b.id === id);
  if (existing) return;
  course.badges.push({ id, name, how, artifact, color, pos, geo });
};

// ---------- 1. Reorder DL-NEURAL ----------
{
  const m = findMod('DL-NEURAL');
  const order = ['tensors', 'autograd-modules', 'layers-activations', 'optimizers-training', 'training-loop'];
  m.checkpoints.sort((a, b) => order.indexOf(a.id) - order.indexOf(b.id));
}

